#ifndef SPI_MASTER_REGS_H_

#define SPI_MASTER_REGS_H_

#include <alt_types.h>
#include <io.h>

#define RESET 0x2000
#define ONESHOT 0x2004
#define CONTROL 0x2008
#define TEMP 0x200C
#define CHAN1 0x2010
#define CHAN2 0x2014
#define CHAN3 0x2018
#define CHAN4 0x201C
#define REFERENCE 0x2020

#define SET_SPI_RESET IOWR_32DIRECT(RESET,0,0x00000001)

#define SET_SPI_ONESHOT IOWR_32DIRECT(ONESHOT,0,0x00000001)

#define SET_SPI_CONTINOUS_ON IOWR_32DIRECT(CONTROL,0,0x00000001)
#define SET_SPI_CONTINOUS_OFF IOWR_32DIRECT(CONTROL,0,0x00000000)

#define GET_SPI_CONTROL IORD_32DIRECT(CONTROL,0)

#define GET_SPI_TEMP IORD_32DIRECT(TEMP,0)
#define GET_SPI_CHAN1 IORD_32DIRECT(CHAN1,0)
#define GET_SPI_CHAN2 IORD_32DIRECT(CHAN2,0)
#define GET_SPI_CHAN3 IORD_32DIRECT(CHAN3,0)
#define GET_SPI_CHAN4 IORD_32DIRECT(CHAN4,0)
#define GET_SPI_REFERENCE IORD_32DIRECT(REFERENCE,0)

#endif
